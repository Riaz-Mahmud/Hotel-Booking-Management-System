    public void ShowHotelDetails()
    {
        try 
        {
            Connection con = SQLConnection.getConnection();
            String query = "SELECT * FROM hotel_info Where hotel_id='"+hotelID+"' ";
            PreparedStatement ps = con.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            if(rs.next())
            {
                jTextArea1.setText(rs.getString(2));
                location.setText(rs.getString(3));
                howroom.setText(rs.getString(4));
                price.setText(rs.getString(5));
                jTextArea2.setText(rs.getString(6));
                jTextArea3.setText(rs.getString(7));
            }
            con.close();

        } 
        catch (Exception e) 
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }
