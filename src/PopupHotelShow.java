    public void UpdateHotelInfo()
    {
        try
        {
            Connection con = SQLConnection.getConnection();
            String sql = "UPDATE hotel_info SET name=?, location=?, rooms=?, price=?, about=?, facility=? WHERE hotel_id='"+hotelID+"'";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, jTextArea1.getText());
            pst.setString(2, location.getText());
            pst.setString(3, howroom.getText());
            pst.setString(4, price.getText());
            pst.setString(5, jTextArea2.getText());
            pst.setString(6, jTextArea3.getText());

            if(pst.executeUpdate() > 0)
            {
                NotificationShow ns = new NotificationShow();
                ns.setVisible(true);
                ns.jLabel2.setText("Update Successfully!");
                this.dispose();   
            }
        con.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }
