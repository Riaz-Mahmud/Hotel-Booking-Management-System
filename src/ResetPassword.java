    void CheckPassword()
    {
        if(NewPass.getText().trim().isEmpty() || RePass.getText().trim().isEmpty())
        {
            if(NewPass.getText().trim().isEmpty())
            {
                NewPassLabel.setText("Empty!");
                
            }
            if(RePass.getText().trim().isEmpty())
            {
                RePassLabel.setText("Empty!");
            }
        }
        if(!NewPass.getText().equals(RePass.getText()))
        {
            RePassLabel.setText("RePass Not Match!");
        }
        else
        {
            UpdatePass();
        }
    }
    
    void UpdatePass()
    {
        try
        {
            Connection con = SQLConnection.getConnection();
            String sql2 = "UPDATE "+tablename+" SET password=? WHERE username='"+getusername+"'";
            PreparedStatement pst2 = con.prepareStatement(sql2);
            pst2.setString(1, RePass.getText());

            if (pst2.executeUpdate() > 0)
            {
                JOptionPane.showMessageDialog(null,"Successful","Password Update",WIDTH);
                NewPass.setText("");
                RePass.setText("");
                username.setText("");
                number.setText("");
                RepassPanel.setVisible(false);
                username.setEditable(true);
                number.setEditable(true);
                                //System.exit(0);
            }
            else 
            {
                JOptionPane.showMessageDialog(null,"Alart!","Password Not Update.\nTry Again",HEIGHT);
            }
            con.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }
