    void CheckOkButton()
    {
        if(username.getText().trim().isEmpty() || number.getText().trim().isEmpty() || email.getText().trim().isEmpty() )
        {
            if(username.getText().trim().isEmpty() )
            {
                userLabel.setText("Username is Empty!");
                username.requestFocus(true);
            }
            if(number.getText().trim().isEmpty())
            {
                NumberLabel.setText("Number is Empty!");
                number.requestFocus(true);
            }
            if(email.getText().trim().isEmpty())
            {
                jLabel12.setText("Empty");
            }
            
        }
        else
        {
            if(username.getText().length()>3)
            {
                String usernameg = username.getText();
                char[] sign =usernameg.toCharArray();
                String sign1 = String.valueOf(sign, 0, 3);
                
                if(sign1.equals("ad-") || sign1.equals("AD-"))
                {
                    this.tablename ="admininfo";
                    OpenReSetPanel(tablename);
                }
                else if(sign1.equals("us-")|| sign1.equals("US-"))
                {
                    this.tablename= "userinfo";
                    OpenReSetPanel(tablename);
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Make sure you typed the user ID \n"
                                                 + "assigned to you by your organization.\n"
                                                 + "It usually looks like XX-XXXXX.", "Invalid Username or Number", WIDTH);
                    username.requestFocus(true);
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Make sure you typed the user ID \n"
                                                 + "assigned to you by your organization.\n"
                                                 + "It usually looks like XX-XXXXX.", "Invalid Username or Number", WIDTH);
            }
        }
    }
    void OpenReSetPanel(String tblename)
    {
        String getTablename = tblename;
        
        try
        {
            Connection con = SQLConnection.getConnection();
            String sql = "Select * From "+getTablename+" Where username=? AND phone=? AND email=?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, username.getText());
            pst.setString(2, number.getText());
            pst.setString(3, email.getText());
            this.getusername = username.getText();
            ResultSet rs = pst.executeQuery();
            if(rs.next())
            {
                
                username.setText("");
                number.setText("");
                email.setText("");
                RepassPanel.setVisible(true);
                NewPass.requestFocus(true);
                OkButton.setEnabled(false);
                username.setEditable(false);
                number.setEditable(false);
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Make sure you typed the user ID \n"
                                                 + "assigned to you by your organization.\n"
                                                 + "It usually looks like XX-XXXXX.", "Invalid Username or Number", WIDTH);
            }
            con.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }
    
