    public void generate_BookedID()  //Create automatic ISBN, its follow the last isbn
    {
        try
        {
            Connection con = SQLConnection.getConnection();
            Statement st = con.createStatement();
            String query = "SELECT booked_id FROM booked ORDER BY booked_id DESC LIMIT 1";
            ResultSet rs = st.executeQuery(query);
            if(rs.next())
            {
                this.bookedid = rs.getInt(1);
                this.nextbookedid = bookedid+1;
                
            }
            else
            {
                nextbookedid = 1111;
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }
