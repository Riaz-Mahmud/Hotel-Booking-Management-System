    public void UpdateLookingFor()
    {
        
        try
        {
            Connection con = SQLConnection.getConnection();
            String sql = "Select * From hotel_info Where hotel_id=?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, hotelId);
            ResultSet rs = pst.executeQuery();
            if(rs.next())
            {
                this.lookingfor=rs.getInt(9);
                int lookingforUp= lookingfor-1;
                try
                {
                    String sql1 = "UPDATE hotel_info SET lookingfor=? WHERE hotel_id='"+hotelId+"'";
                    PreparedStatement pst1 = con.prepareStatement(sql1);
                    pst1.setInt(1, lookingforUp);
                    if(pst1.executeUpdate() > 0)
                    {
                        this.dispose();
                    }
                con.close();
                }
                catch(Exception e)
                {
                    JOptionPane.showMessageDialog(null, e);
                }
                
            }
        con.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }
    
    
